set -o pipefail
export SHELLOPTS

cd "$(dirname "${0}")"

{

  echo '# This file is part of the Spectrum +4 Project.'
  echo '# Licencing information can be found in the LICENCE file'
  echo '# (C) 2021 Spectrum +4 Authors. All rights reserved.'
  echo
  echo "# This file is auto-generated by ${0##*/}." 'DO NOT EDIT!'
  echo
  echo
  echo '.if ROMS_INCLUDE'
  echo '.else'
  echo '  .include "print_w0.s"'
  echo '.endif'
  echo
  echo '.text'
  echo '.align 2'

  i=0
  while [ $i -lt 16 ]; do
    x=$((RANDOM % 0x800))
    y=$((RANDOM % 0x400))
    if [ $x -lt 1728 ] && [ $y -lt 928 ]; then
      printf '%02x 0x%04x 0x%04x\n' $i $x $y
      i=$((i + 1))
    fi
  done | while read i x y; do
    testname="pixel_addr_${i}"
    echo
    echo
    echo "${testname}_setup_regs:"
    echo "  mov     x12, ${y}"
    echo "  mov     x13, ${x}"
    echo "  ret"
    echo
    echo "${testname}_effects_regs:"
    echo "  adrp    x12, display_file"
    echo "  add     x12, x12, :lo12:display_file"
    x14="$(printf '0x%02x\n' $((x % 8)))"
    x11offset="$(printf '%05x\n' $((216 * (20 * (16 * ((927 - y) / 320) + ((927 - y) & 0x0f)) + ((927 - y) % 320 >> 4)) + x / 8)))"
    echo "  add     x11, x12, 0x0${x11offset:2:3}"
    if [ "${x11offset:0:2}" != "00" ]; then
      echo "  add     x11, x11, #0x${x11offset:0:2}000"
    fi
    echo "  mov     x14, #${x14}"
    echo "  ret"
  done
} | ../../../utils/asm-format/asm-format > "test_pixel_addr.gen-s"
